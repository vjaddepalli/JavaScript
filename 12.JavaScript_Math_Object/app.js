//sqrt
let sqrt=Math.sqrt(144);
console.log(`SQRT of 144 is : ${sqrt}`);

//pow
let pow=Math.pow(2,5);
console.log(`2 ^ of 5 is : ${pow}`);

//min
let min=Math.min(10,20,30,40,5,35,66);
console.log(`Minimum Value : ${min}`);

//max
let max=Math.max(10,20,30,40,5,35,66);
console.log(`Maximum Value : ${max}`);

//random
let random=Math.random();
console.log(`Random Number : ${random*10}`);