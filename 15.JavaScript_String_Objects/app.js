//-------String Operations----------------
let msg=String('Hello This is String');

//length
console.log(`String Length: ${msg.length}`);

//uppercase
console.log(msg.toUpperCase());

//lowercase
console.log(msg.toLowerCase());

//substring
console.log(msg.substr(0,5))
console.log(msg.substr(14));

//charAt
console.log(msg.charAt(6));

//charCodeAt(ASCII)
console.log(msg.charCodeAt(6));
